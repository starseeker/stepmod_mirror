    /**
     * Sets the action for the save button
     * The save button will save the contents of the window to the Stepmod properties file
     */
    private void save(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save
        stepMod.setStepmodProperty("CVSEXE",cvsExe.getText());
        stepMod.setStepmodProperty("CVS_RSH",cvsRsh.getText());
        stepMod.setStepmodProperty("SFORGE_USERNAME",userName.getText());
        stepMod.setStepmodProperty("STEPMODROOT",stepmodRoot.getText());
        String protocol = "";
        switch (protocolComboBox.getSelectedIndex()) {
            case 0:
                protocol="ext";
                break;
            case 1:
                protocol="ssh";
                break;
            case 2  :
                protocol="pserver";
                break;
        }
        stepMod.setStepmodProperty("PROTOCOL",protocol);
        stepMod.writeStepmodProperties();
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_save
